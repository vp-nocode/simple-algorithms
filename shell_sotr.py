def shell_sort(arr):
    # Начинаем с большого разрыва и уменьшаем его
    n = len(arr)
    gap = n // 2

    # Пока разрыв больше нуля
    while gap > 0:
        # Выполняем сортировку вставками для подмассивов, образованных элементами на текущем разрыве
        for i in range(gap, n):
            # Сохраняем текущий элемент и создаем индекс для сравнения
            temp = arr[i]
            j = i

            # Сдвигаем элементы, которые больше текущего, на разрыв вперёд
            while j >= gap and arr[j - gap] > temp:
                arr[j] = arr[j - gap]
                j -= gap

            # Вставляем текущий элемент на его правильное место
            arr[j] = temp

        # Уменьшаем разрыв
        gap //= 2

# Пример использования
array = [23, 12, 1, 8, 34, 54, 2, 3]
print("Исходный массив:", array)
shell_sort(array)
print("Отсортированный массив:", array)
